// switch statement

let day = 2;
switch(day){
case 0:
    console.log("sunday");
    
case 1:
    console.log("monday");
    
    case 2:
    console.log("tuesday");
    break;

case 3:
    console.log("wednesday");
    
case 4:
    console.log("thursday");
    
case 5:
    console.log("Friday");
    
case 6:
    console.log("saturday");
    
    default: console.log("invalid day");
}
console.log("test");
