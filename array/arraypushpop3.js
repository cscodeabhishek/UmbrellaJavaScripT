// push pop
let fruits = ['apple', 'orange', 'grapes', 'kiwi'];
console.log(fruits);
// push ---->in the last
fruits.push = ('banana');
console.log(fruits)
// pop-->delete the element from the array from the last index
let poppedFruits = fruits.pop();
console.log('Popped Fruits is',poppedFruits);
console.log(fruits);
