// String indexing
let firstName = "Thakur";
// T h a k u r
// 0 1 2 3 4 5
console.log(firstName[3]); // print the character which is on "3rd" indexing

//length of string
// firstname.length
console.log(firstName.length); // print the length of the string which is '6'

// last Index : length - 1
console.log(firstName[firstName.length - 1]); // print the last character of the string which is 'r'

